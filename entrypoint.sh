#!/bin/bash


DUMP_LOCATION=${DUMP_LOCATION:-/tmp/dump.sql}

echo "Dumping from ${SOURCE_PG_DBNAME}@${SOURCE_PG_HOST}..."
PGHOST=$SOURCE_PG_HOST PGUSER=$SOURCE_PG_USER PGPASSWORD=$SOURCE_PG_PASSWORD PGPORT=$SOURCE_PG_PORT pg_dump $SOURCE_PG_DBNAME > $DUMP_LOCATION

tar -cvzf ${DUMP_LOCATION}.tar.gz $DUMP_LOCATION
rm $DUMP_LOCATION

echo $(ls -la $DUMP_LOCATION)